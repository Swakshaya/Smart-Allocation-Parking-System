# GRADIO UI
# =====================================================

demo = gr.Interface(

    fn=recommend_slot,

    inputs=[

        gr.Textbox(
            label="Vehicle Number"
        ),

        gr.Dropdown(
            ["Car","Bike","Truck"],
            label="Vehicle Type",
            value="Car"
        )

    ],

    outputs=gr.HTML(),

    title="ParkAI - AI Parking System"
)

